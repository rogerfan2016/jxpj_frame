package com.zfsoft.hrm.manoeuvre.exception;

public class ManoeuvreException extends RuntimeException {

	private static final long serialVersionUID = -8451523207083631777L;

	public ManoeuvreException() {
		super();
	}

	public ManoeuvreException(String message, Throwable cause) {
		super(message, cause);
	}

	public ManoeuvreException(String message) {
		super(message);
	}

	public ManoeuvreException(Throwable cause) {
		super(cause);
	}
	
	
	
}
