public class ExportType {
	private String id;
	private String name;
	private String dir;
	/**
	 * 返回
	 */
	public String getId() {
		return id;
	}
	/**
	 * 设置
	 * @param id 
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * 返回
	 */
	public String getName() {
		return name;
	}
	/**
	 * 设置
	 * @param name 
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * 返回
	 */
	public String getDir() {
		return dir;
	}
	/**
	 * 设置
	 * @param dir 
	 */
	public void setDir(String dir) {
		this.dir = dir;
	}
	
	
}
